/**
 * @file logger.hpp
 * @brief Заголовочный файл класса Logger (синглтон для логирования в консоль и файл).
 * 
 * Реализован с использованием только стандартных C-библиотек (stdio.h, time.h и др.).
 * Предоставляет методы для записи сообщений с уровнями важности и временными метками.
 */

#ifndef LOGGER_HPP
#define LOGGER_HPP
#pragma once


// Предварительное объявление структуры FILE из стандартной библиотеки C.
struct _iobuf;
typedef _iobuf FILE;


/**
 * Уровни важности логируемых сообщений.
 * 
 * Используются для фильтрации сообщений: логируются только те, чей уровень >= минимального.
 */
enum LogLevel {
    LOG_LEVEL_DEBUG = 0,
    LOG_LEVEL_INFO,
    LOG_LEVEL_WARNING,
    LOG_LEVEL_ERROR,
    LOG_LEVEL_NONE
};


/**
 * @class Logger
 * @brief Синглтон-класс для логирования сообщений в консоль и файл.
 * 
 * Обеспечивает:
 * 
 * - Потокобезопасную инициализацию.
 * 
 * - Запись с временными метками в формате ГГГГ-ММ-ДД ЧЧ:ММ:СС.
 * 
 * - Фильтрацию по уровню важности.
 * 
 * - Раздельные методы для вывода в консоль (clog) и в файл (flog).
 * 
 * - Запрет копирования и перемещения.
 */
class Logger final
{
private:
    // ---------------------- Поля класса ----------------------

    /**
     * @brief Указатель на файл лога (открыт в режиме добавления "a").
     * 
     * Если файл не удалось открыть, указатель равен nullptr.
     *          Закрывается в деструкторе или при смене файла.
     */
    FILE *__file;

    /**
     * @brief Минимальный уровень важности, при котором сообщение будет записано.
     * 
     * Сообщения с уровнем меньше этого значения игнорируются.
     *          По умолчанию LOG_LEVEL_WARNING.
     */
    LogLevel __min_level;

    // ---------------------- Приватные методы ----------------------

    /**
     * @brief Приватный конструктор (синглтон).
     * @param filename Имя файла лога (по умолчанию "info.log").
     * @param minLevel Минимальный уровень логирования (по умолчанию LOG_LEVEL_WARNING).
     * @details Открывает файл для добавления. Вызывается только из getLogger().
     */
    Logger(const char *filename = "info.log", LogLevel minLevel = LOG_LEVEL_WARNING);

    /**
     * @brief Закрывает файл лога, если он был открыт.
     */
    void __file_close__() noexcept;
    
    /**
     * Форматирует сообщение с временной меткой и уровнем.
     * @param level     Уровень важности.
     * @param message   Текст сообщения (уже сформатированный пользователем).
     * @param formatted Указатель на буфер, куда будет записана итоговая строка.
     * @return          Указатель на тот же буфер (для удобства цепочек вызовов).
     * @note Формат: "[ГГГГ-ММ-ДД ЧЧ:ММ:СС] [УРОВЕНЬ] сообщение\n" 
     * Буфер должен иметь размер не менее FORMATED_STR_SIZE (1024 символа).
     */
    const char* __writeLog__(LogLevel level, const char *message, char *formatted);
    
public:

    /**
     * Деструктор.
     */
    ~Logger();

    /**
     * @brief Статический метод доступа к единственному экземпляру (синглтон).
     * @return Указатель на глобальный объект Logger.
     * @note Реализован через синглтон Майерса.
     */
    static Logger* getLogger();


    /**
     * @brief Устанавливает (или изменяет) файл для записи логов.
     * @param filename Имя файла (путь). Файл открывается в режиме добавления "a".
     * @note Если предыдущий файл был открыт, он закрывается.
     */
    void set_logfile(const char *filename);

    /**
     * @brief Устанавливает минимальный уровень логирования.
     * @param level Новый минимальный уровень (сообщения ниже него игнорируются).
     */
    void set_minlivel(LogLevel level);

    
    /**
     * @brief File log. Записывает сообщение в файл с временной меткой и уровнем.
     * @param level  Уровень важности.
     * @param format Форматная строка в стиле printf (с переменным числом аргументов).
     * @param ...    Аргументы для форматирования.
     */
    void flog(LogLevel level, const char* format, ...);

    /**
     * @brief Console log. Выводит сообщение в консоль с временной меткой и уровнем.
     * @param level  Уровень важности.
     * @param format Форматная строка в стиле printf (с переменным числом аргументов).
     * @param ...    Аргументы для форматирования.
     */
    void clog(LogLevel level, const char* format, ...);


    // ---------------------- Запрет копирования и перемещения ----------------------

    Logger(const Logger&) = delete;
    Logger(Logger&& ) = delete;

    Logger& operator=(const Logger&) = delete;
    Logger& operator=(Logger&&) = delete;
};


#endif // LOGGER_HPP